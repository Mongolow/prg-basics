def input_string(a):
    b = input(a)
    result = str(b)
    return result

def input_int(a):
    b = input(a)
    result = int(b)
    return result

def input_real(a):
    b = input(a)
    result = float(b)
    return result

def input_boolean(a):
    b = input(a)
    result = bool(b)
    return result